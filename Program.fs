open System // 11 Вариант

// Рекурсивная функция для чтения строк и формирования списка их длин
let rec readLines acc =
    printf "Введите строки (пустая строка для завершения): " // Сообщение пользователю
    match Console.ReadLine() with // Читаем строку из консоли
    | null | "" -> List.rev acc  // Если ввод пустой (Enter), возвращаем список в обратном порядке
    | line -> readLines (String.length line :: acc) // Добавляем длину строки в список и продолжаем рекурсию

[<EntryPoint>] // Атрибут, указывающий точку входа в программу
let main argv =
    let lengths = readLines [] // Вызываем функцию чтения строк, передавая пустой список в качестве аккумулятора
    printfn "Длины введённых строк: %A" lengths // Выводим результат
    0 // Возвращаем код завершения программы
