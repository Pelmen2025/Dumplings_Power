open System

// Рекурсивная функция для вычисления суммы чётных цифр числа n
let rec sumEvenDigits n =
    if n = 0 then 0 // Базовый случай: если число 0, возвращаем 0
    else
        let digit = n % 10 // Получаем последнюю цифру числа
        let rest = n / 10 // Убираем последнюю цифру, получаем оставшуюся часть числа
        let sumRest = sumEvenDigits rest // Рекурсивно вызываем функцию для оставшихся цифр
        if digit % 2 = 0 then digit + sumRest // Если цифра чётная, прибавляем её к сумме
        else sumRest // Если нечётная, пропускаем её

// Главная функция программы (точка входа)
[<EntryPoint>]
let main argv =
    printf "Введите натуральное число: " // Просим пользователя ввести число
    let input = Console.ReadLine() // Читаем ввод с клавиатуры
    match Int32.TryParse(input) with // Пробуем преобразовать строку в целое число
    | (true, n) when n > 0 -> // Проверяем, что ввод корректен и число натуральное (больше 0)
        let result = sumEvenDigits n // Вызываем рекурсивную функцию для вычисления суммы чётных цифр
        printfn "Сумма чётных цифр числа %d: %d" n result // Выводим результат
    | _ -> printfn "Ошибка: введите корректное натуральное число!" // Обрабатываем некорректный ввод
    0 // Код завершения программы
